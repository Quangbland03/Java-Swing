    public boolean check2() {
        String name = txtname.getText();
        String ageString = txtage.getText();
        String province = txtprovince.getText();
        String dateString = txtdate.getText();
        String phoneString = txtphone.getText();
        String hoursString = txthours.getText();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane,
                    "Name field cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (!Character.isUpperCase(name.charAt(0))) {
            JOptionPane.showMessageDialog(rootPane, "Name should start with an uppercase letter.",
                     "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (name.matches(".*\\d.*")) { //0-9
            JOptionPane.showMessageDialog(rootPane, "Name should not contain numbers.",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (ageString.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Age field cannot be empty.",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        try {
            int age = Integer.parseInt(ageString);
            if (age <= 15 || age >= 150) {
                JOptionPane.showMessageDialog(rootPane, "Invalid age! Age should be between 16 and 149.",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Invalid age! Age should be a valid integer.",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (province.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Province field cannot be empty.",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (!Character.isUpperCase(province.charAt(0))) {
            JOptionPane.showMessageDialog(rootPane, "Province should start with an uppercase letter.",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (province.matches(".*\\d.*")) {
            JOptionPane.showMessageDialog(rootPane, "Province should not contain numbers.",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (phoneString.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Phone number field cannot be empty.",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (dateString.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Date field cannot be empty.",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (!dateString.matches("^(0[1-9]|[12][0-9]|3[01])/(0[1-9]|1[0-2])/2023$")) {
            JOptionPane.showMessageDialog(rootPane, "Invalid date format! "
                    + "Date should be in the format dd/mm/yyyy and the year must be 2023.",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (hoursString.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Hours field cannot be empty.",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        try {
            double hours = Double.parseDouble(hoursString);
            if (hours < 0 || hours % 0.5 != 0) {
                JOptionPane.showMessageDialog(rootPane, "Invalid hours! Hours should be a "
                        + "positive number and increase by 0.5.", "Error", JOptionPane.ERROR_MESSAGE);
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Invalid hours! Hours should be "
                    + "a valid number.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

