    public void sortbyprice(CustomerDAO cusdao) {
        List<Customer> Customers = cusdao.getCustomers();

        // Sort the students by grade using a comparator
        Collections.sort(Customers, new Comparator<Customer>() {
            @Override
            public int compare(Customer s1, Customer s2) {
                return Double.compare(s1.getTotalprice(), s2.getTotalprice());
            }
        });
        tablemodel.setRowCount(0);
        for (Customer cus : cusdao.getCustomers()) {
            tablemodel.addRow(new Object[]{cus.getName(), cus.getAge(), cus.getProvince(), cus.getPhone(), cus.getRoom(), cus.getDate(), cus.getHours(), cus.getTotalprice()});
        }
    }

