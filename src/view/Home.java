    public void searchbyName(CustomerDAO cusdao) {
        String searchText = txtsearch.getText();
        if (!searchText.isEmpty()) {
            tablemodel.setRowCount(0);
            for (Customer cus : cusdao.searchCustomer(searchText)) {
                tablemodel.addRow(new Object[]{cus.getName(), cus.getAge(), cus.getProvince(), cus.getPhone(), cus.getRoom(), cus.getDate(), cus.getHours(), cus.getTotalprice()});
            }
            JOptionPane.showMessageDialog(rootPane, "Find success");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Please enter search keyword");
        }
    }
