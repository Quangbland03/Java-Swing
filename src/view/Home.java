    public void removebtn(CustomerDAO cusdao) {
        if (cusdao.getCustomers().size() == 0) {
            JOptionPane.showMessageDialog(rootPane, "need to add information to table");
        } else if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(rootPane, "need to select line to remove");
        } else if (selectedIndex > -1) {
            int confirm = JOptionPane.showConfirmDialog(null, "Do you want to remove this line", "Comfirm to remove", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                cusdao.remove(selectedIndex);
                showData(cusdao);
            }
        }
    }
