public boolean validateInputs() {
        String email = txtEmail.getText();
        String phone = txtPhone.getText();
        String password = new String(txtPassword.getPassword());

        // Check email condition
        boolean isEmailValid = email.contains("@");
        if (!isEmailValid) {
            JOptionPane.showMessageDialog(null, 
                    "Invalid email. Please enter a valid email address.");
            return false;
        }

        // Check phone condition
        boolean isPhoneValid = phone.startsWith("0") && phone.length() == 11;
        if (!isPhoneValid) {
            JOptionPane.showMessageDialog(null, "Invalid phone number. "
                    + "Please enter a valid phone number starting with 0 and consisting of 11 digits.");
            return false;
        }

        // Check password condition
        boolean isPasswordValid = password.length() >= 8
                && password.matches(".*[A-Z].*") // Contains at least one uppercase letter
                && password.matches(".*\\d.*") // Contains at least one digit
                && password.matches(".*[!@#$%^&*()].*");  // Contains at least one special character
        if (!isPasswordValid) {
            JOptionPane.showMessageDialog(null, "Invalid password. "
                    + "Please enter a valid password with a minimum of 8 characters, "
                    + "including at least one uppercase letter, one digit, and one special character.");
            return false;
        }

        // All conditions are satisfied
        return true;
    }
