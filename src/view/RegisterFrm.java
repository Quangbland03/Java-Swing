    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if (validateInputs()) {
            var email = txtEmail.getText();
            var phone = txtPhone.getText();
            var password = new String(txtPassword.getPassword());
            var rePassword = new String(txtRePassword.getPassword());
            if (!email.isEmpty() && !phone.isEmpty() && !password.isEmpty()
                    && !rePassword.isEmpty()) {
                if (password.compareTo(rePassword) == 0) {
                    Account acc = new Account(email, password, phone);
                    loginFrm.addAccount(acc);
                    JOptionPane.showMessageDialog(rootPane, "Sign Up Success");
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Can't wait to join again!");
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Do not leave blank");
            }
        }

    }//GEN-LAST:event_btnRegisterActionPerformed

